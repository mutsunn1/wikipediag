"""
全局配置文件
"""

from dataclasses import dataclass
from typing import Optional
import os


@dataclass
class CrawlerConfig:
    """爬虫配置类"""
    
    # 语言配置
    language: str = "zh"  # 默认简体中文，可选: "zh" | "en"
    
    # 输出配置
    output_dir: str = "output"  # 可自定义输出目录
    save_raw_html: bool = False  # 是否保存原始HTML
    save_content: bool = True    # 是否保存提取的内容
    
    # 分类文件夹配置
    create_category_folders: bool = True  # 是否按分类创建文件夹
    
    # Markdown索引配置
    generate_markdown_index: bool = True  # 是否生成Markdown索引
    
    # 爬取配置
    max_pages: int = 50
    pages_per_query: int = 5
    https_proxy: Optional[str] = None  # 可选代理，例如 http://127.0.0.1:7890
    
    @property
    def content_dir(self) -> str:
        """内容保存目录"""
        return os.path.join(self.output_dir, "content")
    
    @property
    def index_dir(self) -> str:
        """索引文件目录"""
        return os.path.join(self.output_dir, "index")
    
    def get_category_dir(self, domain: str, category: str) -> str:
        """获取分类目录路径"""
        # 清理文件夹名称
        safe_domain = self._sanitize_folder_name(domain)
        safe_category = self._sanitize_folder_name(category)
        return os.path.join(self.content_dir, safe_domain, safe_category)
    
    def _sanitize_folder_name(self, name: str) -> str:
        """清理文件夹名称，移除非法字符"""
        import re
        # 替换非法字符
        safe = re.sub(r'[<>:"/\\|?*]', '_', name)
        # 限制长度
        return safe[:100]
    
    def get_text(self, key: str) -> str:
        """获取对应语言的文本"""
        return TRANSLATIONS.get(self.language, TRANSLATIONS["zh"]).get(key, key)


