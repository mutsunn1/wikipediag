# 全局配置实例
_config: Optional[CrawlerConfig] = None


def get_config() -> CrawlerConfig:
    """获取全局配置"""
    global _config
    if _config is None:
        _config = CrawlerConfig()
    return _config


def set_config(config: CrawlerConfig):
    """设置全局配置"""
    global _config
    _config = config


def init_config(
    output_dir: str = "output",
    language: str = "zh",
    max_pages: int = 50,
    **kwargs
) -> CrawlerConfig:
    """
    初始化全局配置
    
    Args:
        output_dir: 输出目录路径
        language: 语言代码 ("zh" 或 "en")
        max_pages: 最大爬取页面数
        **kwargs: 其他配置参数
    
    Returns:
        CrawlerConfig实例
    """
    global _config
    _config = CrawlerConfig(
        output_dir=output_dir,
        language=language,
        max_pages=max_pages,
        **kwargs
    )
    return _config
