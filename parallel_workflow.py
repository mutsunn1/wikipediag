# 使用ParallelAgent的高级工作流
# ============================================

async def parallel_agent_workflow():
    """
    使用ParallelAgent的高级工作流 - 自动分类版本
    """
    print("=" * 70)
    print("🚀 Parallel Agent Workflow - Auto Categorization")
    print("=" * 70)
    
    os.makedirs("output", exist_ok=True)
    
    async with MAS(oxy_space=oxy_space_parallel) as mas:
        query = """Execute Wikipedia crawling workflow for "Computer Science" domain with AUTO-CATEGORIZATION.

Configuration:
- Domain: Computer Science
- Max Pages: 20
- Categorization: AUTO-GENERATED (no predefined categories)

Execution Steps:

1. Call plan_agent to create a plan:
   - domain: "Computer Science"
   - max_pages: 20

2. Use crawl_wikipedia_pages to get pages:
   - queries: based on plan
   - max_pages: 20
   - pages_per_query: 4

3. Call parallel_analyzer to analyze ALL pages in parallel:
   - Each page: extract main_topics, key_concepts, suggested_category_tags
   - Use all 5 analyzers for maximum parallelism

4. Call clustering_agent to AUTO-GENERATE categories:
   - Input: all page analysis results
   - Output: auto_generated_categories based on content similarity
   - Categories should emerge naturally from the content

5. Save to output/cs_auto_clustered_index.json

IMPORTANT: Categories are NOT predefined. Let clustering_agent analyze content and create appropriate categories!

Execute now!"""
        
        print("\n📚 Starting parallel workflow with auto-categorization...")
        print("-" * 70)
        
        try:
            result = await mas.chat_with_agent(payload={"query": query})
            print("\n✅ Workflow complete!")
            result_text = _extract_mas_text(result)
            if result_text:
                print(f"\n📋 Result summary:\n{result_text[:800]}...")
        except Exception as e:
            print(f"\n❌ Error: {str(e)}")
            import traceback
            traceback.print_exc()
    
    print("\n" + "=" * 70)
    print("✨ Parallel workflow with auto-categorization finished!")
    print("=" * 70)


if __name__ == "__main__":
    import sys
    
    mode = sys.argv[1] if len(sys.argv) > 1 else "parallel"
    
    if mode == "batch":
        asyncio.run(batch_analysis_mode())
    else:
        asyncio.run(parallel_agent_workflow())
