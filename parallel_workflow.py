# ============================================
# 批处理模式 - 使用start_batch_processing
# ============================================

async def batch_analysis_mode():
    """
    使用批处理模式进行并行分析 + 自动分类
    """
    
    batch_space = [
        oxy.HttpLLM(
            name="default_llm",
            api_key=os.getenv("DEFAULT_LLM_API_KEY", ""),
            base_url=os.getenv("DEFAULT_LLM_BASE_URL", "https://api.openai.com/v1"),
            model_name=os.getenv("DEFAULT_LLM_MODEL_NAME", "gpt-4o-mini"),
            llm_params={"temperature": 0.2, "max_tokens": 1024},
            semaphore=25,
        ),
        tools.wikipedia_tools,
        tools.file_tools,
        oxy.ReActAgent(
            name="page_analyzer",
            desc="Analyzes Wikipedia page content and extracts topics/concepts",
            prompt=prompts.ANALYSIS_AGENT_PROMPT,
            llm_model="default_llm",
            semaphore=15,
        ),
        oxy.ReActAgent(
            name="clustering_agent",
            desc="Automatically clusters pages into categories",
            prompt=prompts.CLUSTERING_AGENT_PROMPT,
            llm_model="default_llm",
        ),
        oxy.ReActAgent(
            name="master_agent",
            is_master=True,
            sub_agents=["page_analyzer", "clustering_agent"],
            tools=["wikipedia_tools", "file_tools"],
            llm_model="default_llm",
            semaphore=15,
        ),
    ]
    
    print("=" * 70)
    print("🚀 Parallel Batch Analysis + Auto-Categorization Mode")
    print("=" * 70)
    
    os.makedirs("output", exist_ok=True)
    
    async with MAS(oxy_space=batch_space) as mas:
        # Step 1: 爬取页面
        print("\n📥 Step 1: Crawling Wikipedia pages...")
        crawl_query = """Crawl Wikipedia pages about Computer Science.

Search for these topics (max 2 pages each):
- Data Structures: Array, Linked List, Tree
- Algorithms: Sorting, Graph Algorithm, Dynamic Programming
- Systems: Operating System, Database, Network

Use crawl_wikipedia_pages with:
- queries: ["Array data structure", "Linked list", "Tree data structure", "Sorting algorithm", "Graph algorithm", "Dynamic programming", "Operating system", "Database", "Computer network"]
- max_pages: 15
- pages_per_query: 2

Return the complete results with all page contents."""
        
        crawl_result = await mas.chat_with_agent(payload={"query": crawl_query})
        print(f"✅ Crawl complete")
        
        # Step 2: 准备批量分析请求
        print("\n🔍 Step 2: Preparing batch analysis...")
        
        # 示例页面列表 - 实际应从crawl_result解析
        pages_to_analyze = [
            {"title": "Array data structure", "domain": "Computer Science"},
            {"title": "Linked list", "domain": "Computer Science"},
            {"title": "Tree (data structure)", "domain": "Computer Science"},
            {"title": "Sorting algorithm", "domain": "Computer Science"},
            {"title": "Graph theory", "domain": "Computer Science"},
            {"title": "Dynamic programming", "domain": "Computer Science"},
            {"title": "Operating system", "domain": "Computer Science"},
            {"title": "Database", "domain": "Computer Science"},
            {"title": "Computer network", "domain": "Computer Science"},
            {"title": "Hash table", "domain": "Computer Science"},
        ]
        
        analysis_requests = []
        for page in pages_to_analyze:
            req = f"""Analyze this Wikipedia page:
- Title: {page['title']}
- Target Domain: {page['domain']}

First use get_page_content to fetch the page content, then analyze and extract:
- main_topics (3-5 topics)
- key_concepts (5 key concepts)
- suggested_category_tags (3-5 tags for categorization)
- analysis_summary (100-200 words)

Return results in JSON format."""
            analysis_requests.append(req)
        
        # Step 3: 并行批处理分析
        print(f"\n⚡ Step 3: Running parallel batch analysis ({len(analysis_requests)} pages)...")
        
        batch_results = await mas.start_batch_processing(
            analysis_requests,
            return_trace_id=True
        )
        
        print(f"✅ Batch analysis complete! {len(batch_results)} pages analyzed")
        
        # Step 4: 自动聚类分类
        print("\n🗂️ Step 4: Auto-clustering into categories...")
        
        # 构建聚类请求
        analyzed_pages_data = []
        for i, (trace_id, answer) in enumerate(batch_results):
            analyzed_pages_data.append({
                "title": pages_to_analyze[i]["title"],
                "analysis_result": answer
            })
        
        cluster_query = f"""Based on the following analyzed Wikipedia pages about Computer Science,
automatically cluster them into natural categories.

Total pages: {len(analyzed_pages_data)}

Analyzed pages data:
{json.dumps(analyzed_pages_data, indent=2)[:8000]}

Task:
1. Analyze the main_topics and suggested_category_tags from each page
2. Identify natural groupings/clusters (e.g., "Data Structures", "Algorithms", "Systems")
3. Assign each page to the most appropriate category
4. Generate category descriptions

Call clustering_agent with the analyzed data to produce final categorized index.
Then save to output/batch_auto_clustered.json"""
        
        cluster_result = await mas.chat_with_agent(payload={"query": cluster_query})
        print(f"✅ Clustering complete!")
        
        # 保存结果
        print("\n💾 Step 5: Saving results...")
        output = {
            "mode": "batch_parallel_auto_cluster",
            "total_pages": len(batch_results),
            "analysis_results": [
                {"page": pages_to_analyze[i]["title"], "trace_id": r[0], "result": r[1][:500]}
                for i, r in enumerate(batch_results)
            ],
            "clustering_result": _extract_mas_text(cluster_result)
        }
        
        with open("output/batch_auto_clustered.json", "w", encoding="utf-8") as f:
            json.dump(output, f, ensure_ascii=False, indent=2)
        
        print("✅ Results saved to output/batch_auto_clustered.json")
    
    print("\n" + "=" * 70)
    print("✨ Batch analysis with auto-categorization complete!")
    print("=" * 70)


